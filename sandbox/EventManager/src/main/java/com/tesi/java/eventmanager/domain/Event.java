package com.tesi.java.eventmanager.domain;

import java.util.Date;

/**
 *
 * @author mikerocha
 */
public class Event {
    private long id;
    
    private String title;
    private Date date;

    public Event() {}
    
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }   
}
